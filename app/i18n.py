def get_lang() -> str:
    return st.session_state.get("lang", "fr")


def set_lang(lang: str) -> None:
    st.session_state.lang = lang if lang in LANGUAGE_OPTIONS else "fr"


def t(key: str, **kwargs) -> str:
    lang = get_lang()
    text = TRANSLATIONS.get(lang, TRANSLATIONS["fr"]).get(key, key)
    return text.format(**kwargs) if kwargs else text


def nutrient_label(name: str) -> str:
    lang = get_lang()
    return NUTRIENT_LABELS.get(lang, NUTRIENT_LABELS["fr"]).get(name, name)


def food_label(name: str) -> str:
    lang = get_lang()
    return FOOD_LABELS.get(lang, FOOD_LABELS["fr"]).get(name, name)


def format_food_names(names: list[str]) -> str:
    return ", ".join(food_label(name) for name in names)


def activity_label(code: str) -> str:
    return t(f"activity_{code}")


def activity_description(code: str) -> str:
    return t(f"activity_desc_{code}")


def strength_label(code: str) -> str:
    return t(f"strength_{code}")


def goal_label(code: str) -> str:
    return t(f"goal_{code}")
